import axios from 'axios';

import { IBPMNProcessInfo } from '@/shared/model/bpmn-process-info.model';

const baseApiUrl = 'api/bpmn-process-infos';

export default class BPMNProcessInfoService {
  public find(id: number): Promise<IBPMNProcessInfo> {
    return new Promise<IBPMNProcessInfo>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IBPMNProcessInfo): Promise<IBPMNProcessInfo> {
    return new Promise<IBPMNProcessInfo>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IBPMNProcessInfo): Promise<IBPMNProcessInfo> {
    return new Promise<IBPMNProcessInfo>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: IBPMNProcessInfo): Promise<IBPMNProcessInfo> {
    return new Promise<IBPMNProcessInfo>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
